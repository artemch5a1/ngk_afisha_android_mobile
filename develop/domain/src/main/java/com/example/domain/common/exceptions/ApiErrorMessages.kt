package com.example.domain.common.exceptions

object ApiErrorMessages {

    private val messages = mapOf(
        400 to "Неверные данные, проверьте ввод",
        401 to "Пользователь не аутентифицирован",
        403 to "Нет прав доступа",
        404 to "Ресурс не найден",
        409 to "Конфликт данных, возможно дубликат",
        422 to "Данные корректны синтаксически, но не проходят бизнес-валидацию",
        429 to "Слишком много запросов, попробуйте позже",
        500 to "Ошибка на сервере, попробуйте позже",
        503 to "Сервис временно недоступен",
        504 to "Превышено время ожидания ответа сервера"
    )

    fun getMessage(code: Int): String {
        return messages[code] ?: "Неизвестная ошибка"
    }
}